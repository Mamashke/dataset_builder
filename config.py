# config.py — центральный файл конфигурации проекта dataset_builder.
# Все пути и параметры обработки данных задаются здесь, чтобы остальные
# модули импортировали константы из одного места и не содержали "магических" значений.

from pathlib import Path

# BASE_DIR — абсолютный путь к корневой папке проекта.
# Path(__file__).parent берёт директорию, в которой лежит сам config.py,
# что гарантирует корректную работу независимо от рабочей директории запуска.
BASE_DIR = Path(__file__).parent

# ---------------------------------------------------------------------------
# Пути к исходным (сырым) данным
# ---------------------------------------------------------------------------

# Папка с оригинальными видеозаписями реальной камеры (mp4-файлы)
RAW_REAL_DIR = BASE_DIR / "data" / "raw" / "real"

# Папка с видеозаписями из симулятора AirSim (mp4-файлы)
RAW_AIRSIM_DIR = BASE_DIR / "data" / "raw" / "airsim"

# ---------------------------------------------------------------------------
# Пути к обработанным кадрам (результат работы modules/loader.py)
# ---------------------------------------------------------------------------

# Куда сохраняются извлечённые кадры из реальных видео
FRAMES_REAL_DIR = BASE_DIR / "data" / "processed" / "frames" / "real"

# Куда сохраняются извлечённые кадры из AirSim-видео
FRAMES_AIRSIM_DIR = BASE_DIR / "data" / "processed" / "frames" / "airsim"

# ---------------------------------------------------------------------------
# Пути к аннотациям и финальному датасету
# ---------------------------------------------------------------------------

# Папка для хранения файлов разметки (JSON, XML, YOLO txt и т.п.)
ANNOTATIONS_DIR = BASE_DIR / "data" / "processed" / "annotations"

# Папка с изображениями финального датасета (после балансировки и аугментации)
DATASET_IMAGES_DIR = BASE_DIR / "data" / "processed" / "dataset" / "images"

# Папка с метками финального датасета в формате YOLO (*.txt рядом с каждым изображением)
DATASET_LABELS_DIR = BASE_DIR / "data" / "processed" / "dataset" / "labels"

# ---------------------------------------------------------------------------
# Настройки модуля loader (извлечение кадров из видео)
# ---------------------------------------------------------------------------

# Шаг выборки кадров: сохраняется каждый N-й кадр.
# Значение 5 означает ~6 кадров в секунду при исходных 30 fps,
# что достаточно для обучения и не создаёт избыточного дублирования.
FRAME_SAMPLE_RATE = 5

# Ширина и высота, к которым приводятся все извлечённые кадры.
# 640×640 — стандартный входной размер для моделей семейства YOLO.
TARGET_WIDTH = 640
TARGET_HEIGHT = 640

# Формат сохраняемых кадров. "jpg" даёт хорошее соотношение качество/размер.
# Можно сменить на "png" для сохранения без потерь.
FRAME_FORMAT = "jpg"

# ---------------------------------------------------------------------------
# Система проектов
# ---------------------------------------------------------------------------

# Корневая папка всех проектов — рядом с main.py
PROJECTS_DIR = BASE_DIR / "projects"

# ---------------------------------------------------------------------------
# Пути для логирования
# ---------------------------------------------------------------------------

# Папка для хранения лог-файлов. При каждом запуске main.py создаётся новый
# файл с временной меткой; pipeline.log ведётся непрерывно между запусками.
LOGS_DIR = BASE_DIR / "logs"

# ---------------------------------------------------------------------------
# Настройки модуля exporter (экспорт финального датасета)
# ---------------------------------------------------------------------------

# Корневая папка для всех форматов экспорта
EXPORT_DIR = BASE_DIR / "export"

# Имена классов в порядке их числовых идентификаторов (0, 1, 2, ...)
CLASS_NAMES = ['person']

# ---------------------------------------------------------------------------
# Настройки модуля augmentor (аугментация кадров)
# ---------------------------------------------------------------------------

# Качество JPEG при сохранении аугментированных кадров (0–100).
# 85 даёт хорошее визуальное качество при ~3× меньшем размере файла
# по сравнению с дефолтным значением cv2 (95).
AUGMENT_QUALITY = 85

# Если True, аугментированные кадры принудительно ресайзятся до
# TARGET_WIDTH × TARGET_HEIGHT перед сохранением.
# Это позволяет значительно сократить размер датасета, если исходные
# кадры уже имеют нужный размер (640×640) — ресайз будет no-op,
# но качество JPEG всё равно применится.
AUGMENT_RESIZE = True

# ---------------------------------------------------------------------------
# Настройки модуля generator (DCGAN)
# ---------------------------------------------------------------------------

# Разрешение изображений для обучения и генерации GAN: 64, 128 или 256.
# Большие значения дают более детальные изображения, но требуют больше памяти.
GAN_IMAGE_SIZE = 64

# Размер батча для обучения GAN.
# RTX 3060 6 ГБ: максимум 16–32. RTX A6000 48 ГБ: до 128.
GAN_BATCH_SIZE = 16

# ---------------------------------------------------------------------------
# Настройки модуля balancer (фильтрация и балансировка датасета)
# ---------------------------------------------------------------------------

# Минимальная допустимая средняя яркость кадра (0–255).
# Кадры темнее этого порога считаются непригодными для обучения:
# на них объекты практически не различимы.
MIN_BRIGHTNESS = 30

# Минимальная «резкость» кадра по методу Лапласа (variance of Laplacian).
# Чем выше значение, тем резче изображение. Кадры с дисперсией ниже порога
# считаются размытыми и исключаются из датасета.
BLUR_THRESHOLD = 100.0

# Целевое соотношение негативных примеров к позитивным.
# POS_NEG_RATIO = 3 означает: на каждый позитивный кадр (с объектами)
# допускается не более 3 негативных (без объектов).
# Это предотвращает перекос датасета в сторону фонов и ускоряет обучение.
POS_NEG_RATIO = 3
